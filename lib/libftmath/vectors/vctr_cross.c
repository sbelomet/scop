#include "libftmath.h"

t_vec3	ft_vec3_cross(const t_vec3 v1, const t_vec3 v2)
{
	t_vec3	v;

	v = ft_vec3(0, 0, 0);
	v.v[0] = v1.v[1] * v2.v[2] - v1.v[2] * v2.v[1];
	v.v[1] = v1.v[2] * v2.v[0] - v1.v[0] * v2.v[2];
	v.v[2] = v1.v[0] * v2.v[1] - v1.v[1] * v2.v[0];
	return (v);
}
